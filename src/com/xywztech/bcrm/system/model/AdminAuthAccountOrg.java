package com.xywztech.bcrm.system.model;
import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;


/**
 * The persistent class for the ADMIN_AUTH_ACCOUNT_ORG database table.
 * 
 */
@Entity
@Table(name="ADMIN_AUTH_ACCOUNT_ORG")
public class AdminAuthAccountOrg implements Serializable {
	private static final long serialVersionUID = 1L;

	@Id
	@GeneratedValue(generator = "CommonSequnce", strategy = GenerationType.SEQUENCE)
	private Long id;

	@Column(name="ACCOUNT_ID")
	private Long accountId;

	@Column(name="APP_ID")
	private String appId;

	@Column(name="ORG_ID")
	private String orgId;

    public AdminAuthAccountOrg() {
    }

	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getAccountId() {
		return this.accountId;
	}

	public void setAccountId(Long accountId) {
		this.accountId = accountId;
	}

	public String getAppId() {
		return this.appId;
	}

	public void setAppId(String appId) {
		this.appId = appId;
	}

	public String getOrgId() {
		return this.orgId;
	}

	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}

}